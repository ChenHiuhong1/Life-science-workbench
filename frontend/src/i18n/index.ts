export type DictKey = keyof typeof dict.en;

interface I18nState {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: DictKey) => string;
}

export const useI18n = create<I18nState>()(
  persist(
    (set) => ({
      lang: 'en',
      setLang: (lang) => set({ lang }),
      t: (key) => dict.en[key] ?? key,
    }),
    { name: 'workbench-i18n' }
  )
);
